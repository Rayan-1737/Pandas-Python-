import pandas as pd


# =========================================================
# WHAT IS A DATAFRAME?
# =========================================================

# A DataFrame is a 2-dimensional table in Pandas.
#
# It has:
# 1. Rows
# 2. Columns
#
# Similar to:
# - Excel Sheet
# - SQL Table
# - CSV File
#
# Example:
#
#    Name   Age   Marks
# 0  Rahul   20      85
# 1  Amit    21      90
# 2  Neha    19      95
#
# Rows → Horizontal
# Columns → Vertical





# =========================================================
# CREATING DATAFRAMES
# =========================================================



# =========================================================
# A) Creating DataFrame From Dictionary
# =========================================================

data = {
    'Name': ['Rahul', 'Amit', 'Neha'],
    'Age': [20, 21, 19],
    'Marks': [85, 90, 95]
}

df = pd.DataFrame(data)

print(df)

# Output:

#     Name  Age  Marks
# 0  Rahul   20     85
# 1   Amit   21     90
# 2   Neha   19     95





# =========================================================
# B) Creating DataFrame From List
# =========================================================

data = [
    ['Rahul', 20],
    ['Amit', 21]
]

df = pd.DataFrame(data, columns=['Name', 'Age'])

print(df)

# Output:

#     Name  Age
# 0  Rahul   20
# 1   Amit   21





# =========================================================
# C) Creating DataFrame From List of Dictionaries
# =========================================================

data = [
    {'Name': 'Rahul', 'Age': 20},
    {'Name': 'Amit', 'Age': 21}
]

df = pd.DataFrame(data)

print(df)

# Output:

#     Name  Age
# 0  Rahul   20
# 1   Amit   21





# =========================================================
# D) Creating DataFrame From NumPy Array
# =========================================================

import numpy as np

arr = np.array([
    [1, 2],
    [3, 4]
])

df = pd.DataFrame(arr, columns=['A', 'B'])

print(df)

# Output:

#    A  B
# 0  1  2
# 1  3  4





# =========================================================
# E) Custom Index
# =========================================================

data = {
    'Name': ['Rahul', 'Amit', 'Neha']
}

df = pd.DataFrame(data, index=['a', 'b', 'c'])

print(df)

# Output:

#     Name
# a  Rahul
# b   Amit
# c   Neha





# =========================================================
# F) Creating Empty DataFrame
# =========================================================

df = pd.DataFrame()

print(df)

# Output:

# Empty DataFrame
# Columns: []
# Index: []







# =========================================================
# ACCESSING DATA IN DATAFRAME
# =========================================================



# =========================================================
# A) Access Single Column
# =========================================================

data = {
    'Name': ['Rahul', 'Amit', 'Neha'],
    'Marks': [85, 90, 95]
}

df = pd.DataFrame(data)

print(df['Name'])

# Output:

# 0    Rahul
# 1     Amit
# 2     Neha





# =========================================================
# B) Access Multiple Columns
# =========================================================

print(df[['Name', 'Marks']])

# Output:

#     Name  Marks
# 0  Rahul     85
# 1   Amit     90
# 2   Neha     95





# =========================================================
# C) Access Row Using loc[]
# =========================================================

print(df.loc[0])

# Output:

# Name     Rahul
# Marks       85





# =========================================================
# D) Access Row Using iloc[]
# =========================================================

print(df.iloc[1])

# Output:

# Name     Amit
# Marks      90





# =========================================================
# E) Access Specific Value
# =========================================================

print(df.loc[0, 'Name'])

# Output:

# Rahul





# =========================================================
# F) Access Multiple Rows
# =========================================================

print(df.iloc[0:2])

# Output:

#     Name  Marks
# 0  Rahul     85
# 1   Amit     90









# =========================================================
# IMPORTANT ATTRIBUTES OF DATAFRAME
# =========================================================



# =========================================================
# A) .shape
# =========================================================

print(df.shape)

# Output:

# (3, 2)

# Means:
# 3 rows
# 2 columns





# =========================================================
# B) .columns
# =========================================================

print(df.columns)

# Output:

# Index(['Name', 'Marks'], dtype='object')

# Returns column names





# =========================================================
# C) .index
# =========================================================

print(df.index)

# Output:

# RangeIndex(start=0, stop=3, step=1)

# Returns row indexes





# =========================================================
# D) .dtypes
# =========================================================

print(df.dtypes)

# Output:

# Name     object
# Marks     int64

# Shows datatype of each column





# =========================================================
# E) .values
# =========================================================

print(df.values)

# Output:

# [['Rahul' 85]
#  ['Amit' 90]
#  ['Neha' 95]]

# Returns NumPy array





# =========================================================
# F) .size
# =========================================================

print(df.size)

# Output:

# 6

# Total elements:
# 3 rows × 2 columns = 6





# =========================================================
# G) .ndim
# =========================================================

print(df.ndim)

# Output:

# 2

# DataFrame is 2-dimensional





# =========================================================
# H) .T
# =========================================================

print(df.T)

# Transposes rows into columns





# =========================================================
# I) .empty
# =========================================================

print(df.empty)

# Output:

# False

# Checks whether DataFrame is empty







# =========================================================
# VIEWING DATA
# =========================================================



# =========================================================
# A) head()
# =========================================================

print(df.head())

# Shows first 5 rows





# =========================================================
# B) tail()
# =========================================================

print(df.tail())

# Shows last 5 rows





# =========================================================
# C) sample()
# =========================================================

print(df.sample(1))

# Returns random rows







# =========================================================
# INFORMATION METHODS
# =========================================================



# =========================================================
# A) info()
# =========================================================

print(df.info())

# Gives:
# - column names
# - datatype
# - non-null count
# - memory usage





# =========================================================
# B) describe()
# =========================================================

print(df.describe())

# Gives statistics:
# - mean
# - min
# - max
# - std
# etc.









# =========================================================
# ADDING DATA
# =========================================================



# =========================================================
# A) Adding New Column
# =========================================================

