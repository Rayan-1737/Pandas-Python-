# =========================================================
# GROUPBY OPERATIONS
# =========================================================

data = {
    'Department': ['IT', 'HR', 'IT', 'HR'],
    'Salary': [50000, 40000, 60000, 45000]
}

df = pd.DataFrame(data)





# =========================================================
# A) groupby()
# =========================================================

print(df.groupby('Department')['Salary'].mean())

# Output:

# Department
# HR    42500
# IT    55000

# Groups data and performs operations









# =========================================================
# MERGING DATAFRAMES
# =========================================================

df1 = pd.DataFrame({
    'ID': [1, 2],
    'Name': ['Rahul', 'Amit']
})

df2 = pd.DataFrame({
    'ID': [1, 2],
    'Marks': [85, 90]
})





# =========================================================
# A) merge()
# =========================================================

print(pd.merge(df1, df2, on='ID'))

# Combines DataFrames









# =========================================================
# CONCATENATING DATAFRAMES
# =========================================================

print(pd.concat([df1, df2]))

# Stacks DataFrames









