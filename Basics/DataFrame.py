print(df)

# Output:

#     Name  Marks Grade
# 0  Rahul     85     A
# 1   Amit     90    A+
# 2   Neha     95    A+





# =========================================================
# B) Adding Calculated Column
# =========================================================

df['Bonus'] = df['Marks'] + 5

print(df)

# Output:

# Bonus column added









# =========================================================
# UPDATING DATA
# =========================================================



# =========================================================
# A) Updating Single Value
# =========================================================

df.loc[0, 'Marks'] = 100

print(df)





# =========================================================
# B) Updating Entire Column
# =========================================================

df['Marks'] = df['Marks'] + 10

print(df)









# =========================================================
# DELETING DATA
# =========================================================



# =========================================================
# A) Delete Column
# =========================================================

df.drop('Bonus', axis=1)

# axis=1 means column





# =========================================================
# B) Delete Row
# =========================================================

df.drop(0)

# Deletes row index 0









# =========================================================
# FILTERING DATA
# =========================================================



# =========================================================
# A) Single Condition
# =========================================================

print(df[df['Marks'] > 90])

# Students with marks > 90





# =========================================================
# B) Multiple Conditions
# =========================================================

print(df[(df['Marks'] > 80) & (df['Grade'] == 'A+')])

# & = AND
# | = OR









# =========================================================
# SORTING DATA
# =========================================================



# =========================================================
# A) Sort By Column
# =========================================================

print(df.sort_values('Marks'))





# =========================================================
# B) Descending Order
# =========================================================

print(df.sort_values('Marks', ascending=False))





# =========================================================
# C) Sort By Index
# =========================================================

print(df.sort_index())









# =========================================================
# MISSING VALUES
# =========================================================

import numpy as np

data = {
    'Name': ['Rahul', 'Amit', np.nan],
    'Marks': [85, np.nan, 95]
}

df = pd.DataFrame(data)

print(df)

# Output:

#     Name  Marks
# 0  Rahul   85.0
# 1   Amit    NaN
# 2    NaN   95.0





# =========================================================
# A) isnull()
# =========================================================

print(df.isnull())

# Checks missing values





# =========================================================
# B) isna()
# =========================================================

print(df.isna())

# Same as isnull()





# =========================================================
# C) dropna()
# =========================================================

print(df.dropna())

# Removes rows with missing values





# =========================================================
# D) fillna()
# =========================================================

print(df.fillna(0))

# Fills NaN with 0









